void onPinActivation(uint16_t key)
{
    switch (key)
    {
    case GP3:
        uint8_t current_layer = get_highest_layer(layer_state);

        switch (current_layer)
        {
        case 0:
            tap_code(KC_AUDIO_MUTE);
            break;

        case 1:
            tap_code(KC_WWW_SEARCH);
            break;
        }

        break;
    }
}

void matrix_scan_user(void)
{
    static bool last_state[NUM_LISTENERS] = { false };

    for(int i = 0; i < NUM_LISTENERS; i++)
    {
        if(last_state[i]) continue;

        if(gpio_read_pin(specialKeys[i]))
        {
            onPinActivation(specialKeys[i]);
        }

    }
}
